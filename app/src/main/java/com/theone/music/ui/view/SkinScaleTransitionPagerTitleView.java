import android.content.Context;

import com.theone.mvvm.core.widge.indicator.SkinPagerTitleView;

/**
 * @author The one
 * @date 2022-01-10 13:15
 * @describe TODO
 * @email 625805189@qq.com
 * @remark
 */
public class SkinScaleTransitionPagerTitleView extends SkinPagerTitleView {

    private float mMinScale = 0.75f;

    public SkinScaleTransitionPagerTitleView(Context context) {
        super(context);
    }


    @Override
    public void onEnter(int index, int totalCount, float enterPercent, boolean leftToRight) {
        super.onEnter(index, totalCount, enterPercent, leftToRight);
        setScaleX(mMinScale + (1.0f - mMinScale) * enterPercent);
        setScaleY(mMinScale + (1.0f - mMinScale) * enterPercent);
    }

    @Override
    public void onLeave(int index, int totalCount, float leavePercent, boolean leftToRight) {
        super.onLeave(index, totalCount, leavePercent, leftToRight);
        setScaleX(1.0f + (mMinScale - 1.0f) * leavePercent);
        setScaleY(1.0f + (mMinScale - 1.0f) * leavePercent);
    }


    public float getMinScale() {
        return mMinScale;
    }

    public void setMinScale(float minScale) {
        mMinScale = minScale;
    }

}
