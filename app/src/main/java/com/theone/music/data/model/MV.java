public class MV {

    private String cover;
    private String url;
    private String name;
    private String singer;

    public MV() {
    }

    public MV(String cover, String url, String name, String singer) {
        this.cover = cover;
        this.url = url;
        this.name = name;
        this.singer = singer;
    }

    public String getCover() {
        return cover;
    }

    public void setCover(String cover) {
        this.cover = cover;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSinger() {
        return singer;
    }

    public void setSinger(String singer) {
        this.singer = singer;
    }

    public String fullName(){
        return singer+"-"+name;
    }


}
